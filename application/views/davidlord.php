      <p>David is the owner and manager of Air House Music Academy and Air House Studios.  He has been teaching guitar for the last 13 years.  David has his B.A. in Music Performance from Friends University.  From 2008-2011, David was the guitar professor at Friends University, teaching guitar lessons, directing guitar ensembles, directing jazz combos and teaching guitar class.  David has performed extensively in the Wichita area as a jazz and classical guitarist, performing at countless restaurants, events, shows, concerts and weddings.  He is also a prolific composer and creative guitar player.</p><br />  
      <p>In 2005, he released his first album with his original band, Solagget.  He performed extensively with Solagget from 2003 through 2011, playing venues and festivals throughout the midwest and west coast.  David has been heavily involved with the creative music scene in Wichita, performing at events such as Knob New Music Festival, Fisch Haus Jazz Series, ICT Fest, LIV Fest and the Wichita Creative Music Society Concert Series.</p><br />  
      <p>David is also currently pursuing recording engineering and is one of the assistant engineers at Air House Studios.  David is a founding partner of Air House Records, an independent record label that releases music from the Wichita area.  David is constantly writing, recording and producing his own music.  He is currently releasing solo guitar music under the name Francis Moss,  playing in an experimental jazz trio called Elemental Trio and is one of the main creative forces of the music and art collective, The Wonder Revolution.  All of Davids albums are released on Air House Records.</p><br />
