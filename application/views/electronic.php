      	<p>Beginning a journey into electronic music can be frustrating, confusing, and expensive.  How do you decide which software to buy?  What about gear?  The market is absolutely flooded with choices, and with as sophisticated as the technology is these days, you still can't "buy" your way into producing and performing quality music.  However, this will not stop most people from spending countless hours in front of the computer and thousands of dollars on stuff only to finally realize that electronic music is hard work.  Relax, though--Someone has already cleared a path through this overwhelming terrain for you.</p><br />
      	<p>Through customized instruction, <em>Approaching Electronic Music</em> will help you develop a solid base of knowledge to help you transform your goals of electronic music production and performance into reality, regardless of the genre you're interested in.  The purpose of this class will not be to simply make "computer music" that imitates what's hot right now, but to best equip each individual to realize his or her fullest artistic potential.  In other words, we do not see the synthesizer, sequencer, sampler, MIDI controller, or turntable as instruments of conformity, but rather as extensions of the creative body and mind.</p><br />
      	<p>Electronic music is enriching and accessible to students of all ages and backgrounds. Its fundamentals help nurture a lasting passion for mathematics, engineering, art, and, of course, melody and rhythm. The limitless possibilities of the medium engage the modern imagination while offering insight into the future of sound. Call today to schedule an initial consultation!</p><br />
