<br /><br /><br />
<div class="row">
  <div class="span12 quote2 offset3"><p>In a study conducted by Dr. Timo Krings, pianists and non-musicians of the same age and sex were required to perform complex sequences of finger movements. Their brains were scanned using a technique called "functional magnetic resource imaging" (fMRI) which detects the activity levels of brain cells. The non-musicians were able to make the movements as correctly as the pianists, but less activity was detected in the pianists' brains. Thus, compared to non-musicians, the brains of pianists are more efficient at making skilled movements. These findings show that musical training can enhance brain function.</p><br />
  <div class="cite">  Weinberger, Norm. <em>The Impact of Arts on Learning</em>. MuSICa Research Notes 7, no. 2 (Spring 2000). Reporting on Krings, Timo et al. <em>Cortical Activation Patterns during Complex Motor Tasks in Piano Players and Control Subjects. A Functional Magnetic Resonance Imaging Study</em>. Neuroscience Letters 278, no. 3 (2000): 189-93.
