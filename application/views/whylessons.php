<div class="row">
  <div class="span12 quote1">
    <p>Researchers at the University of Montreal used various brain imaging techniques to investigate brain activity during musical tasks and found that sight-reading musical scores and playing music both activate regions in all four of the cortex's lobes; and that parts of the cerebellum are also activated during those tasks.</p><br />
    <div class="cite">Sergent, J., Zuck, E., Tenial, S., and MacDonall, B. (1992). <em>Distributed Neural Network Underlying Musical Sight Reading and Keyboard Performance</em>. Science, 257, 106-109.
    </div>
