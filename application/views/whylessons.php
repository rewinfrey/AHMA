<div class="row">
  <div class="span12 quote2 offset3"><p> Researchers in Leipzig found that brain scans of musicians showed larger planum temporale (a brain region related to some reading skills) than those of non-musicians. They also found that the musicians had a thicker corpus callosum (the bundle of nerve fibers that connects the two halves of the brain) than those of non-musicians, especially for those who had begun their training before the age of seven.</p><br />
  <div class="cite">Schlaug, G., Jancke, L., Huang, Y., and Steinmetz, H. (1994). <em>In Vivo Morphometry of Interhem Ispheric Assymetry and Connectivity in Musicians</em>. In I. Deliege (Ed.), Proceedings of the 3d international conference for music perception and cognition (pp. 417-418). Liege, Belgium.
