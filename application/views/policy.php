        <li>When making the payment, if students know in advance that they will not be able to attend a lesson in the upcoming month, they are allowed one excused absence per month.  Students should notify the teacher of the absence and withhold the amount of one lesson from their payment.  This must be done at the first lesson of the month.  After payment is made, the lesson times are reserved with the teacher and excused absences will not be granted due to student cancellation.</li>  
        <br />
        <li>Students will not have to pay for lessons that are cancelled by the teacher.</li>
        <br />
        <li>If a lesson has been payed for and is cancelled by the teacher, the student will be given a credit for the following month.</li> 
        <br />
        <li>Make up lessons are at the discretion of the teacher.  For a make up lesson to be considered, the teacher must be given at least 24 hours notice of the student's absence and be able to arrange a make up time that is suitable with the teacher's schedule.  Make up lessons are not guaranteed and will not be given to a student that cancels within 24 hours of the lesson.</li>
