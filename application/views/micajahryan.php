  <h4 class="orange">Audio Recording Classes Now Offered</h4>
    <p>Audio Recording may require at least as much discipline and commitment as learning to play an instrument well. The number of skills involved and mental acuity needed is incomprehensible to the average person. The best way to learn this art is to sit at the feet of a person who has mastered the art and observe closely. Air House Music Academy offers the chance to have one-on-one instruction with someone who has spent years honing that craft.</p>
