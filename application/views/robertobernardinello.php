    <div class="span9">
      <p>Roberto began to play in 1991 under the guide of Piero Orsini (Gipsy Kings - Lisa Hunt). In 1992 he began his first playing experiences, performing in top 40 and rhythm & blues bands. In 1993 he joined a jazz quartet and played around the Milan (Italy) area in clubs and venues. From 1993 to 1995 he played in various fusion and funk band projects.</p><br />

		<p>In 1996, Roberto joined "Diatriba," a flamenco-jazz band lead by virtuoso guitarist Marco Porcu. In the same year this quartet played at Sant Anna Arresi Jazz Festival and opened for Noa. This festival included the Herbie Hancock Quartet, Pat Metheny Trio, Michel Petrucciani Trio and Joshua Redman Sextet. With the same band he performed for three years on Italian TV as well as in major clubs throughout Italy.</p><br />

		<p>In 2003, he moved to London to study at the prestigious Trinity College of Music. There he resumed collaboration with Marco Porcu and also created the "MP Project." This group has performed in clubs such as "J2K" and "Circle in the Round" in London as well as various venues in Kansas and California.</p><br /> 

		<p>Currently Roberto is based in Wichita, Kansas. He leads many ensembles and is a staple of its night scene. Recent collaborations include such artists as: Harry James Big Band, The Fabulous Flippers, Gary Foster, Earl Dumler, Christian Howes, Jay Daversa, Jerry Hahn, Philippe Bertaud, Joseph Vincelli, Mike Longo, Tom Fowler, Bill Caldwell, Roger Lewis, Scott Strecker & Richard Johnson. Roberto holds a teaching position at Butler Community College in El Dorado Kansas.</p><br />
		<p>In 2009 he started a collaboration with Surine Basses as the custom relations director and is the bass he plays almost exclusively.
		Roberto just released his first CD - <a href="http://bernardinello.com/Music.html" target="_blank">ONE</a></p><br />
