    <div class="span9">
      <p>In addition to teaching <a href="/lessons/wind">clarinet and saxophone lessons</a> Bill also helps direct the Air House <a href="/ensembles/jazz">Jazz Ensembles</a>. We are lucky to have someone of Bill's caliber teaching with us. Bill has been a professional music educator and a professional musician for more than 40 years after completing a Bachelor of Music Education degree from Nebraska Wesleyan University (1969 ) and a Master of Music degree from the University of Nebraska in Lincoln (1977).</p><br />
      <p>Bill spent his first eleven years of teaching in Nebraska High schools and his last 29 years teaching in Kansas. He served as band director in Manhattan, Newton, and as a woodwind and jazz specialist for the Maize school district. During his career, his marching, concert and jazz bands have won many state and national awards. His school bands have performed in official functions for three United States Presidents. He has served as an adjunct instructor in clarinet performance and jazz improvisation at various colleges and universities including The University of Nebraska, Kansas State University and Friends University.</p><br />
      <p>Originally trained as a classical clarinetist, he has performed with many artists including Clark Terry, Mel Torme, Johnny Carson, Bob Hope, Red Skelton, Ray Charles, Louie Bellson, Ed Shaunessy, various symphony orchestras and big bands including the Tommy Dorsey Orchestra. During his teaching tenure at the Maize Kansas school district, Bill and his teaching partner Randy Crow enjoyed national prominence with the Maize 6th grade jazz band and it's alums including performances in New York for the International Association for Jazz Education and most recently performing with Bobby McFerrin. Mr. Harshbarger has been very actively involved with The Kansas Music Educator's Association. He has served in various leadership capacities for KMEA including District President, District Band Chairman, and State Jazz Chairperson.  Bill has enjoyed two terms as President for the Kansas unit of The International Association for Jazz Education. He also served on the Jazz Educator's association's international curriculum resource team. He is a member of Phi Beta Mu International Bandmasters Fraternity. He is in demand as a music judge and clinician throughout the Midwest.</p><br /> 
      <p>In 2003, Bill was honored by KMEA as the outstanding elementary music educator for south-central Kansas. In the fall of 2005, Bill was honored by the Wichita Arts Council and the City of Wichita as the Arts Educator of the year.</p><br />  
      <p>He has just recently retired from public school teaching. Bill has recently been serving as an Adjunct Instructor in Jazz Studies at Hutchinson Community College. He is active with his private jazz studio at Air House Music Academy, his professional playing, and continues to be an arts activist.</p><br />
