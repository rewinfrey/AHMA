    <p>Making his home in Wichita, Kansas, Steve Hatfield is one of the most in demand musicians in the Midwest. For over 25 years, he has performed in almost every musical setting imaginable. Everything from various Rock and Jazz groups to the Wichita Symphony and Music Theatre of Wichita. Steve has logged countless hours in recording studios performing on numerous albums and commercial jingles. Recently, he released his first album as a leader, "Just Be Wally" on Digital Boy Records.</p><br /> 
